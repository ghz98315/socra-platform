#!/usr/bin/env node
import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const bookDir = path.join(repoRoot, 'book');
const outputDir = path.join(repoRoot, 'apps', 'landing', 'lib', 'generated');
const outputFile = path.join(outputDir, 'bookChapterRegistry.generated.ts');

function extractChapterArticle(html) {
  const articleMatch = html.match(/<article[^>]*class="chapter"[^>]*>([\s\S]*?)<\/article>/i);
  return articleMatch?.[1]?.trim() ?? null;
}

function getChapterFileSortOrder(fileName) {
  const chapterMatch = fileName.match(/^ch(\d+)\.html$/i);
  if (chapterMatch) {
    return Number.parseInt(chapterMatch[1], 10);
  }

  if (fileName.toLowerCase() === 'appendix.html') {
    return Number.MAX_SAFE_INTEGER;
  }

  return Number.MAX_SAFE_INTEGER - 1;
}

function compareChapterFiles(a, b) {
  const aOrder = getChapterFileSortOrder(a);
  const bOrder = getChapterFileSortOrder(b);
  return aOrder - bOrder || a.localeCompare(b, 'en');
}

async function main() {
  const entries = await readdir(bookDir, { withFileTypes: true });
  const htmlFiles = entries
    .filter((entry) => entry.isFile() && (/^ch\d+\.html$/i.test(entry.name) || /^appendix\.html$/i.test(entry.name)))
    .map((entry) => entry.name)
    .sort(compareChapterFiles);

  const registryEntries = [];

  for (const fileName of htmlFiles) {
    const absolutePath = path.join(bookDir, fileName);
    const html = await readFile(absolutePath, 'utf8');
    const article = extractChapterArticle(html);
    if (!article) {
      throw new Error(`Missing <article class="chapter"> in ${fileName}`);
    }

    const id = fileName.replace(/\.html$/i, '');
    registryEntries.push({
      id,
      repoPath: `book/${fileName}`,
      relativeFilePathFromSource: `../../../book/${fileName}`,
    });
  }

  const registryObject = registryEntries
    .map((entry) => {
      return `  ${entry.id}: {\n    repoPath: ${JSON.stringify(entry.repoPath)},\n    relativeFilePathFromSource: ${JSON.stringify(entry.relativeFilePathFromSource)},\n  },`;
    })
    .join('\n');

  const fileContent = `// This file is auto-generated by scripts/import-book-chapters.mjs.\n// Do not edit manually. Update book/*.html files and rerun \"pnpm.cmd book:import\".\n\nexport const GENERATED_FILE_BACKED_BOOK_CHAPTERS = {\n${registryObject}\n} as const;\n`;

  await mkdir(outputDir, { recursive: true });
  await writeFile(outputFile, fileContent, 'utf8');

  console.log(`Generated ${path.relative(repoRoot, outputFile)} with ${registryEntries.length} imported chapter source(s).`);
  for (const entry of registryEntries) {
    console.log(`- ${entry.id} <- ${entry.repoPath}`);
  }
}

main().catch((error) => {
  console.error('[book:import] failed:', error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
